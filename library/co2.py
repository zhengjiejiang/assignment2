def get_Co2_condition(Co2):
    
    if 1600 < Co2 < 2100:
        print ("BAD")

    elif 1100 < Co2 < 1500 :
        print ("MEDIOCRE")

    elif 900 < Co2 < 1000 :
        print ("FAR")

    elif 700 < Co2 < 900 :
        print ("GOOD")

    elif 400 < Co2 < 600 :
        print ("EXCELLENT")
